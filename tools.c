#include "ft_printf.h"

void	ft_putspace(int *g_res)
{
	ft_putchar_fd(' ', 1);
	(*g_res)++;
}

void	ft_putzero(int *g_res)
{
	ft_putchar_fd('0', 1);
	(*g_res)++;
}
